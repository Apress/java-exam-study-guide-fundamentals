package com.readlearncode.programmeexecution;

public class switchexample {
	
	
	private void switchexample() {
	
	String message;
	String airlineCode = "SFO";
	switch(airlineCode) {
		case "SFO":
			message = "Welcome to San Francisco";
			break;
		case "LAX":
			message = "Welcome to Los Angeles";
			break;
		default:
			message = "Where do you want to go?";
	}
	
	
	
	
	
	}
	
	

}
